def parse_diarization_output(text: str) -> TuneResult:
    """Parse the diarization output and extract evaluation metrics."""
    result = TuneResult(params={})

    # Parse segments: 【S1】 or 【S2】 followed by text
    segments = []
    current_speaker = None
    current_lines = []

    for line in text.split("\n"):
        if line.startswith("【"):
            # Save previous segment
            if current_speaker is not None:
                for seg_text in current_lines:
                    segments.append((current_speaker, seg_text))

            # Parse new speaker
            match = re.match(r"【(S\d+)】", line)
            if match:
                current_speaker = match.group(1)
                remaining = line[match.end():].strip()
                current_lines = [remaining] if remaining else []
            else:
                current_speaker = None
                current_lines = []
        elif current_speaker is not None:
            stripped = line.strip()
            if stripped:
                current_lines.append(stripped)

    # Don't forget the last segment
    if current_speaker is not None and current_lines:
        for seg_text in current_lines:
            segments.append((current_speaker, seg_text))

    # Also try to parse the output format that shows segments with timestamps
    # If no segments found via the 【S1】 format, try the raw text
    if not segments:
        # The output might be just one block of text with a single speaker prefix
        if text.strip().startswith("【"):
            first_line = text.strip().split("\n")[0]
            match = re.match(r"【(S\d+)】", first_line)
            if match:
                speaker = match.group(1)
                segments.append((speaker, text.strip()))

    # Build speaker sequence
    seq = [s[0] for s in segments]

    # Count speakers
    speakers = set(s[0] for s in segments)
    result.speaker_count = len(speakers)
    result.segments = segments
    result.seq = seq

    # Calculate speaker balance (rough estimate based on segment count)
    total_segments = len(segments)
    s1_count = sum(1 for s in segments if s[0] == "S1")
    s2_count = sum(1 for s in segments if s[0] == "S2")
    result.balance_s1 = s1_count / total_segments if total_segments > 0 else 0
    result.balance_s2 = s2_count / total_segments if total_segments > 0 else 0

    return result


def damerau_levenshtein(a: List[str], b: List[str]) -> int:
    """Compute the Damerau-Levenshtein distance between two sequences."""
    n, m = len(a), len(b)
    if n == 0:
        return m
    if m == 0:
        return n

    d = [[0] * (m + 1) for _ in range(n + 1)]
    for i in range(n + 1):
        d[i][0] = i
    for j in range(m + 1):
        d[0][j] = j

    for i in range(1, n + 1):
        for j in range(1, m + 1):
            cost = 0 if a[i - 1] == b[j - 1] else 1
            d[i][j] = min(
                d[i - 1][j] + 1,       # deletion
                d[i][j - 1] + 1,       # insertion
                d[i - 1][j - 1] + cost  # substitution
            )
            if i > 1 and j > 1 and a[i - 1] == b[j - 2] and a[i - 2] == b[j - 1]:
                d[i][j] = min(d[i][j], d[i - 2][j - 2] + cost)  # transposition

    return d[n][m]


def normalize_sequence(seq: List[str]) -> List[str]:
    """Normalize speaker sequence: merge consecutive same-speaker runs."""
    if not seq:
        return []
    normalized = [seq[0]]
    for s in seq[1:]:
        if s != normalized[-1]:
            normalized.append(s)
    return normalized


def evaluate(result: TuneResult) -> float:
    """Evaluate the diarization quality and return a score (0-100)."""
    score = 0.0
    details = []

    # 1. Speaker count (40 pts)
    if result.speaker_count == 2:
        score += 40
        details.append(f"speakers=2 ✓ (+40)")
    elif result.speaker_count == 1:
        details.append(f"speakers=1 ✗ (+0)")
    else:
        score += 10
        details.append(f"speakers={result.speaker_count} (+10, expected 2)")

    # 2. Speaker balance (20 pts)
    if result.speaker_count == 2:
        s1_pct = min(result.balance_s1, result.balance_s2)
        s2_pct = max(result.balance_s1, result.balance_s2)
        if s1_pct >= 0.2:
            score += 20
            details.append(f"balance S1={result.balance_s1:.0%} S2={result.balance_s2:.0%} ✓ (+20)")
        elif s1_pct >= 0.1:
            score += 10
            details.append(f"balance S1={result.balance_s1:.0%} S2={result.balance_s2:.0%} ⚠ (+10)")
        else:
            details.append(f"balance S1={result.balance_s1:.0%} S2={result.balance_s2:.0%} ✗ (+0)")

    # 3. Segment count (20 pts) — compare to ground truth
    gt_norm = normalize_sequence(GROUND_TRUTH_SEQ)
    detected_norm = normalize_sequence(result.seq)
    gt_seg_count = len(gt_norm)
    detected_seg_count = len(detected_norm)

    if detected_seg_count >= gt_seg_count * 0.3 and detected_seg_count <= gt_seg_count * 1.5:
        score += 20
        details.append(f"segments={detected_seg_count} (gt={gt_seg_count}) ✓ (+20)")
    elif detected_seg_count >= gt_seg_count * 0.2 and detected_seg_count <= gt_seg_count * 2.0:
        score += 10
        details.append(f"segments={detected_seg_count} (gt={gt_seg_count}) ⚠ (+10)")
    else:
        details.append(f"segments={detected_seg_count} (gt={gt_seg_count}) ✗ (+0)")

    # 4. Sequence similarity (20 pts) — compare normalized sequences
    if result.speaker_count == 2 and detected_seg_count > 0:
        # Align: compare detected vs ground truth at the normalized level
        gt_norm = normalize_sequence(GROUND_TRUTH_SEQ)
        detected_norm = normalize_sequence(result.seq)

        # Take the first N segments for comparison
        min_len = min(len(gt_norm), len(detected_norm))
        if min_len > 0:
            dist = damerau_levenshtein(gt_norm[:min_len], detected_norm[:min_len])
            max_dist = min_len
            similarity = 1.0 - (dist / max_dist)
            seq_score = similarity * 20
            score += seq_score
            details.append(f"seq_similarity={similarity:.0%} (+{seq_score:.0f})")
        else:
            details.append(f"seq_similarity=N/A (+0)")

    result.score = score
    result.details = " | ".join(details)
    return score


def run_trial(params: dict) -> Optional[TuneResult]:
    """Run a single trial with the given parameters."""
    print(f"\n  Config: threshold={params['clustering_threshold']}, "
          f"minSeg={params['embedding_minSegmentDuration']}, "
          f"minOn={params['segmentation_minDurationOn']}, "
          f"minOff={params['segmentation_minDurationOff']}, "
          f"onset={params['segmentation_speechOnsetThreshold']}, "
          f"stepRatio={params['segmentation_stepRatio']}")

    # Generate and write config
    config_body = build_config(params)
    write_diarizer_swift(config_body)

    # Build
    build_time = build_project()
    if build_time < 0:
        return None

    # Run
    print(f"  Build: {build_time:.1f}s, Running diarization...", end="", flush=True)
    run_start = time.time()
    output = run_diarization()
    run_time = time.time() - run_start

    if output is None:
        return None

    print(f" {run_time:.1f}s", flush=True)

    # Parse and evaluate
    result = parse_diarization_output(output)
    result.params = params
    result.build_time = build_time
    result.run_time = run_time

    return result


