        seg_min_duration_off=params["segmentation_minDurationOff"],
        seg_step_ratio=params["segmentation_stepRatio"],
        seg_speech_onset_threshold=params["segmentation_speechOnsetThreshold"],
        seg_speech_offset_threshold=params["segmentation_speechOffsetThreshold"],
        emb_min_segment_duration=params["embedding_minSegmentDuration"],
        clus_threshold=params["clustering_threshold"],
        clus_warm_start_fa=params["clustering_warmStartFa"],
        clus_warm_start_fb=params["clustering_warmStartFb"],
        post_min_gap=params["postProcessing_minGap"],
