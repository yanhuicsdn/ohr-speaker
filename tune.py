def write_diarizer_swift(config_body: str) -> None:
    """Write the modified Diarizer.swift with the given config body."""
    # Read the current file
    with open(DIARIZER_SWIFT, "r") as f:
        content = f.read()

    # Replace the defaultDiarizerConfig function body
    # Find the function start and end
    start_marker = "func defaultDiarizerConfig() -> OfflineDiarizerConfig {"
    end_marker = "\nfunc runDiarization"

    start_idx = content.find(start_marker)
    end_idx = content.find(end_marker, start_idx)

    if start_idx == -1 or end_idx == -1:
        print("ERROR: Could not find defaultDiarizerConfig in Diarizer.swift")
        sys.exit(1)

    new_content = content[:start_idx] + config_body + content[end_idx:]

    with open(DIARIZER_SWIFT, "w") as f:
        f.write(new_content)


